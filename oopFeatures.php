<h2>Why we use OOP?</h2>

<ul>
    <li>Abstraction</li>
    <li>
        <ul>
            <li>Combining multiple smaller operations to single one so that they can be referred to by name</li>
        </ul>
    </li>
    <li>Encapsulation (Information Hiding)</li>
    <ul>
        <li>Separating implementation via interfaces</li>
    </ul>
    <li>Inheritance</li>
    <ul>
        <li>Defining object data types as extensions and for restrictions from other object types</li>
    </ul>
    <li>Polymorphism</li>
    <ul>
        <li>Using the same name to invoke different operations of an object of different data types</li>
    </ul>
</ul>

